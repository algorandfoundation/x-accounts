const fs = require('fs')
const path = require('path')
const { AlgorandClient } = require('@algorandfoundation/algokit-utils')

const tealSrc = path.resolve(__dirname, '../../evm-logicsig/smart_contracts/artifacts/algo-x-evm/AlgoXEvmLsig.teal')
const outFile = path.resolve(__dirname, '../src/generated/lsig-template.ts')

// Two distinct 20-byte sentinels with no overlap. The first is written into the
// generated artifact (kept as a marker for the OWNER slot); the second is used
// only to verify that the OWNER slot is the *only* part of the bytecode that
// varies with the template parameter.
const SENTINEL_A = new Uint8Array(20)
const SENTINEL_B = new Uint8Array(20)
for (let i = 0; i < 20; i++) {
  SENTINEL_A[i] = i % 2 === 0 ? 0xee : 0xff
  SENTINEL_B[i] = i % 2 === 0 ? 0x55 : 0xaa
}

function getAlgorandClient() {
  if (process.env.ALGOD_SERVER) {
    return AlgorandClient.fromConfig({
      algodConfig: {
        server: process.env.ALGOD_SERVER,
        port: process.env.ALGOD_PORT,
        token: process.env.ALGOD_TOKEN ?? '',
      },
    })
  }
  return AlgorandClient.mainNet()
}

function findUniqueSubsequence(haystack, needle) {
  let foundAt = -1
  outer: for (let i = 0; i <= haystack.length - needle.length; i++) {
    for (let j = 0; j < needle.length; j++) {
      if (haystack[i + j] !== needle[j]) continue outer
    }
    if (foundAt !== -1) return -2 // multiple matches
    foundAt = i
  }
  return foundAt
}

function bytesToBase64(bytes) {
  return Buffer.from(bytes).toString('base64')
}

async function main() {
  if (!fs.existsSync(tealSrc)) {
    throw new Error(`TEAL source not found at ${tealSrc} — run copy-teal.js first.`)
  }
  const teal = fs.readFileSync(tealSrc, 'utf8')

  const algorand = getAlgorandClient()

  let compiledA, compiledB
  try {
    ;[compiledA, compiledB] = await Promise.all([
      algorand.app.compileTealTemplate(teal, { TMPL_OWNER: SENTINEL_A }),
      algorand.app.compileTealTemplate(teal, { TMPL_OWNER: SENTINEL_B }),
    ])
  } catch (err) {
    const where = process.env.ALGOD_SERVER ?? 'https://mainnet-api.algonode.cloud (default)'
    throw new Error(
      `Failed to compile TEAL via algod (${where}): ${err.message}\n` +
        `Set ALGOD_SERVER / ALGOD_PORT / ALGOD_TOKEN to point at a reachable algod node.`,
    )
  }

  const programA = compiledA.compiledBase64ToBytes
  const programB = compiledB.compiledBase64ToBytes

  if (programA.length !== programB.length) {
    throw new Error(
      `Sanity check failed: compiled programs differ in length (${programA.length} vs ${programB.length}).`,
    )
  }

  const offsetA = findUniqueSubsequence(programA, SENTINEL_A)
  if (offsetA === -1) throw new Error('Sentinel A not found in compiled program — TEAL template structure changed.')
  if (offsetA === -2) throw new Error('Sentinel A appeared more than once — pick a different sentinel pattern.')

  const offsetB = findUniqueSubsequence(programB, SENTINEL_B)
  if (offsetB !== offsetA) {
    throw new Error(`Sanity check failed: sentinel B at offset ${offsetB}, expected ${offsetA}.`)
  }

  for (let i = 0; i < programA.length; i++) {
    if (i >= offsetA && i < offsetA + 20) continue
    if (programA[i] !== programB[i]) {
      throw new Error(
        `Sanity check failed: byte ${i} differs between sentinel compiles (${programA[i]} vs ${programB[i]}). ` +
          `The template should only vary inside the OWNER slot.`,
      )
    }
  }

  const contents =
    `// AUTOGENERATED by scripts/build-lsig-template.js — do not edit.\n` +
    `\n` +
    `/** Compiled lsig program template. The 20 bytes at LSIG_OWNER_OFFSET are a sentinel\n` +
    ` * placeholder for the EVM owner address; everything else is fixed and used for pattern matching. */\n` +
    `export const LSIG_TEMPLATE_BASE64 = ${JSON.stringify(bytesToBase64(programA))};\n` +
    `\n` +
    `/** Byte offset within the compiled program where the 20-byte EVM address sits. */\n` +
    `export const LSIG_OWNER_OFFSET = ${offsetA};\n` +
    `\n` +
    `/** Length of the EVM address slot in bytes. */\n` +
    `export const LSIG_OWNER_LENGTH = 20;\n` +
    `\n` +
    `/** Total length of the compiled lsig program in bytes. */\n` +
    `export const LSIG_TEMPLATE_LENGTH = ${programA.length};\n`

  fs.writeFileSync(outFile, contents)
  console.log(
    `Wrote ${path.relative(process.cwd(), outFile)} ` + `(program=${programA.length}B, ownerOffset=${offsetA})`,
  )
}

main().catch((err) => {
  console.error(err.message)
  process.exit(1)
})
